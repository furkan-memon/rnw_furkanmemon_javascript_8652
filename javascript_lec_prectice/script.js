let x = 10
let y = 20

console.log("Addition:", x + y)
console.log("Multiplication:", x * y)
console.log("Division:", x / y)
console.log("Subtraction:", x - y)
console.log("Modulus (Remainder):", x % y)
console.log("Exponent (x²):", x ** 2)
console.log("Exponent (y²):", y ** 2)
console.log("Post-increment (x++):", x++)
console.log("Pre-increment (++y):", ++y)
console.log("Post-decrement (x--):", x--)
console.log("Pre-decrement (--y):", --y)